u16 Util::calcLpfFreq(f32 scale)
{
    scale = sead::Mathf::clamp2(0.0f, scale, 1.0f);

    u16 freq = 0;

    // freq = static_cast<u16>(Mathf::pow(2.0f, (scale - 1.0f) * 10.0f) * 32000.0f);
    // A table is used instead of the above.
    if (scale < cCalcLpfFreqIntercept)
    {
        freq = cCalcLpfFreqTable[0];
    }
    else if (scale >= 0.9f)
    {
        freq = cCalcLpfFreqTable[cCalcLpfFreqTableSize - 1];
    }
    else
    {
        s32 idx = static_cast<s32>((scale - cCalcLpfFreqIntercept) / (0.1f / 3.0f));
        freq = cCalcLpfFreqTable[idx];
    }

    return freq;
}

f32 Util::calcPanRatio(f32 pan, const PanInfo& info, OutputMode mode)
{
    pan = (sead::Mathf::clamp2(-1.0f, pan, 1.0f) + 1.0f) / 2.0f; // Clamp to 0.0 - 1.0 scale

    const f32* table = cPanTableTable[(u32)info.curve];

    f32 ratio = table[static_cast<s32>(pan * cPanTableMax + 0.5f)];

    if (info.centerZeroFlag)
        ratio /= table[cPanTableCenter]; // Set the center to a multiple of 1.0

    if (info.zeroClampFlag)
        ratio = sead::Mathf::clamp2(0.0f, ratio, 1.0f);
    else
        ratio = sead::Mathf::clamp2(0.0f, ratio, 2.0f);

    return ratio;
}

f32 Util::calcSurroundPanRatio(f32 surroundPan, const PanInfo& info)
{
    surroundPan = sead::Mathf::clamp2(0.0f, surroundPan, 2.0f ) / 2.0f; // Clamp to 0.0 - 1.0 scale

    const f32* table = cPanTableTable[(u32)info.curve];

    f32 ratio = table[static_cast<s32>(surroundPan * cPanTableMax + 0.5f)];

    ratio = sead::Mathf::clamp2(0.0f, ratio, 2.0f);

    return ratio;
}

f32 Util::calcPitchRatio(s32 pitch_)
{
    static const s32 cRange = cPitchDivisionRange * cOctaveDivision;

    s32 pitch = pitch_;

    s32 octave = 0;

    while (pitch < 0)
    {
        octave--;
        pitch += cRange;
    }

    while (pitch >= cRange)
    {
        octave++;
        pitch -= cRange;
    }

    s32 note = pitch / cPitchDivisionRange;
    pitch -= note * cPitchDivisionRange;

    f32 ratio = 1.0f;

    while (octave > 0)
    {
        ratio *= 2.0f;
        octave--;
    }

    while (octave < 0)
    {
        ratio /= 2.0f;
        octave++;
    }

    if (note != 0)
        ratio *= cNoteTable[note];

    if (pitch != 0)
    {
        SEAD_ASSERT(pitch >= 0 && pitch <= Util::cPitchDivisionRange);
        ratio *= cPitchTable[pitch];
    }

    return ratio;
}

f32 Util::calcVolumeRatio(f32 dB)
{
    dB = sead::Mathf::clamp2(-90.4f, dB, 6.0f);

    s32 index = static_cast<s32>(dB * 10) - cVolumeDbMin;
    return cDecibel2RatioTable[index];
}

u32 Util::getSampleByByte(u32 byte, SampleFormat format)
{
    u32 samples = 0;
    u32 frac;

    switch (format)
    {
        case SampleFormat::PcmS8:
            samples = byte;
            break;

        case SampleFormat::PcmS16:
            samples = (byte >> 1);
            break;

        case SampleFormat::DspAdpcm:
            samples = (byte / 8) * 14;
            frac = (byte % 8);

            if (frac != 0)
                samples += (frac - 1) * 2;

            break;

        default:
            SEAD_ASSERT_MSG(false, "Invalid format");
            break;
    }

    return samples;
}

u32 Util::getByteBySample(u32 samples, SampleFormat format)
{
    u32 byte = 0;
    u32 frac;

    switch (format)
    {
        case SampleFormat::PcmS8:
            byte = samples;
            break;

        case SampleFormat::PcmS16:
            byte = (samples << 1);
            break;

        case SampleFormat::DspAdpcm:
            byte = (samples / 14) * 8;
            frac = (samples % 14);

            if (frac != 0)
                byte += ((frac + 1) >> 1) + 1;

            break;

        default:
            SEAD_ASSERT_MSG(false, "Invalid format");
            break;
    }

    return byte;
}

